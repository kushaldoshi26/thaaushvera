{
    protected $fillable = [
        'code', 'type', 'value', 'min_order_amount', 'usage_limit', 
        'used_count', 'valid_from', 'valid_until', 'is_active'
    ];

    protected $casts = [
        'valid_from' => 'datetime',
        'valid_until' => 'datetime',
        'is_active' => 'boolean'
    ];
}
